    private void btnSeleccionarComponentesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSeleccionarComponentesMouseClicked
        
        Componentes.aireAcondicionado = new Aire(cboTipoAireAcondicionado.getSelectedItem().toString());
        if(cboControlRemoto.getSelectedItem().toString().equalsIgnoreCase("Si")){
            Componentes.aireAcondicionado.getControlRemoto().setColor(cboColorControl.getSelectedItem().toString());
            Componentes.aireAcondicionado.getControlRemoto().setMarca(cboMarcaControl.getSelectedItem().toString());
            Componentes.aireAcondicionado.getControlRemoto().setCantidadDeCodigos(cboCodigosControl.getSelectedItem().toString());
        }else{
            Componentes.aireAcondicionado.setControlRemoto(null);
        }
        
        if(cantidadValida()){
            sonidoFondo.Stop();
            this.dispose();
            Componentes.main(args);
        }else
            JOptionPane.showMessageDialog(this, "La cantidad debe ser\nun nÃºmero natural\ndistinto de 0."
                                              ,"Cantidad invÃ¡lida",1,new ImageIcon(getClass().getResource("/Imagenes/cantidadInvalida.gif")));
        
        
    }//GEN-LAST:event_btnSeleccionarComponentesMouseClicked

    private void cboControlRemotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboControlRemotoActionPerformed
        if(cboControlRemoto.getSelectedItem().toString().equalsIgnoreCase("No")){
            cboMarcaControl.setEnabled(false);
            cboCodigosControl.setEnabled(false);
            cboColorControl.setEnabled(false);
        }else{
            cboMarcaControl.setEnabled(true);
            cboCodigosControl.setEnabled(true);
            cboColorControl.setEnabled(true);
        }
    }//GEN-LAST:event_cboControlRemotoActionPerformed

